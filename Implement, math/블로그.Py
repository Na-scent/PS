# ----------------------------------------------------------------------------------------------

# 문제

# 찬솔이는 
# $X$일 동안 가장 많이 들어온 방문자 수와 그 기간들을 알고 싶다.

# 찬솔이를 대신해서 
# $X$일 동안 가장 많이 들어온 방문자 수와 기간이 몇 개 있는지 구해주자.

# ----------------------------------------------------------------------------------------------

# 입력

# 첫째 줄에 블로그를 시작하고 지난 일수 
# $N$와 
# $X$가 공백으로 구분되어 주어진다.

# 둘째 줄에는 블로그 시작 
# $1$일차부터 
# $N$일차까지 하루 방문자 수가 공백으로 구분되어 주어진다.

# ----------------------------------------------------------------------------------------------

# 출력

# 첫째 줄에 
# $X$일 동안 가장 많이 들어온 방문자 수를 출력한다. 만약 최대 방문자 수가 0명이라면 SAD를 출력한다.

# 만약 최대 방문자 수가 0명이 아닌 경우 둘째 줄에 기간이 몇 개 있는지 출력한다.

# ----------------------------------------------------------------------------------------------

# 배열 슬라이싱을 이용하면 시간초과(반복문 처럼 돌아감).
# 따라서 배열 슬라이싱 없이 풀 수 있는 방법 생각해야 함.

n, x = map(int, input().split())

days = list(map(int, input().split()))
cnt = 1
sumday = sum(days[:x])
maxday = sumday

for i in range(n-x):
  sumday = sumday - days[i] + days[i+x]
  if sumday > maxday:
    maxday = sumday
    cnt = 1
  elif sumday == maxday:
    cnt += 1
    
if sumday == 0:
  print("SAD")
else:
  print(maxday)
  print(cnt)